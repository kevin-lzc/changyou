package com.example.happytravel.bean;

public class jpBean {
    private String name;
    private int imageId;

    public jpBean(String name, int imageId){
        this.name = name;
        this.imageId = imageId;

    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }
} 


